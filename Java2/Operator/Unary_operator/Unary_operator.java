public class Unary_operator{
    public static void main(String[] args){
        int marks  = +1;
        System.out.println("Value of marks : " +  marks );

        marks--;
        System.out.println("Value of marks : " +  marks );

        marks++;
        System.out.println("Value of marks : " + marks );

        boolean marks_Res = false;
        System.out.println("Value of marks : " + marks);
        System.out.println("Value of marks : " + (!marks_Res));

    }
}