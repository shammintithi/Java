public class Operator{
    public static void main(String[] args){

        int marks = 20 + 10;

        int Deduct_marks = 20 - 5;

        int multiplication = 20 * 9;

        int Divide_marks = 20 / 2;

        float modulus = 18 % 2;

        modulus = 10 % 7;

        System.out.println("Results add: " + marks);

        System.out.println("Results subtraction: " + Deduct_marks);

        System.out.println("Results Multiplication: " + multiplication);

        System.out.println("Results divide: " + Divide_marks);

        System.out.println("Results modulus: " + modulus);

    }
}