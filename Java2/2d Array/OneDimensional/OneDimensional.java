public class OneDimensional{
    public static void main(String[] args) {
        int[] array = new int[3];

        array[0] = 10;
        array[1] = 20;
        array[2] = 30;

        System.out.println("Array " + array[0]);
        System.out.println("Array " + array[1]);
        System.out.println("Array " + array[2]);
    }
}